        [b1,b2,b3,b4,b5,b6,b7,b8,b9,b10,c1,c2,c3,c4]=getcoeff_10(index);
    case 20
        [b1,b2,b3,b4,b5,b6,b7,b8,b9,b10,c1,c2,c3,c4]=getcoeff_20(index);
