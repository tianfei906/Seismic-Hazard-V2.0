function[lny,sigma,tau,phi]=AkkarBoomer2007(To,M,Rjb,media,SOF,damping)

% Akkar, S. and Bommer, J. J., [2007]
% Prediction of elastic displacement response spectra in Europe and the Middle East” Earthquake Engineering
% and Structural Dynamics, Vol. 36, No. 10, pp.1275–1301.

st = dbstack;
    [isadmisible,units] = isIMadmisible(To,st(1).name,[nan nan],[nan nan],[0.05 4],[nan nan]);
if isadmisible==0
    lny   = nan(size(M));
    sigma = nan(size(M));
    tau   = nan(size(M));
    phi   = nan(size(M));
    return
end

if To==0.00
    [log10y,sigma,tau,phi] = gmpe(0,Rjb,M,SOF,media,damping);
    
    % log base change
    lny   = log10y * log(10);
    sigma = sigma  * log(10);
    phi   = phi    * log(10);
    tau   = tau    * log(10);
    
    % unit convertion
    lny  = lny+log(units);
    
    return
end

To      = real(To);
period  = linspace(0.05,4.00,80);               % Matrix where are all the periods from the paper
T_lo    = max(period(period<=To));              % Identify the period that is below for the later interpolation
T_hi    = min(period(period>=To));              % Identify the period that is above for the later interpolation
index   = find(abs((period - T_lo)) < 1e-6);    % Identify the period from the ones evaluated in the paper

if T_lo==T_hi
    [log10y,sigma,tau,phi]             = gmpe(index,Rjb,M,SOF,media,damping);
else
    [log10y_lo,~,phi_lo,tau_lo]        = gmpe(index   ,Rjb,M,SOF,media,damping);
    [log10y_hi,~,phi_hi,tau_hi]        = gmpe(index+1 ,Rjb,M,SOF,media,damping);
    x           = log([T_lo;T_hi]);
    Y_sa        = [log10y_lo,log10y_hi]';
    Y_phi       = [phi_lo,phi_hi]';
    Y_tau       = [tau_lo,tau_hi]';
    log10y      = interp1(x,Y_sa,log(To))';
    phi         = interp1(x,Y_phi,log(To))';
    tau         = interp1(x,Y_tau,log(To))';
    sigma       = sqrt(phi.^2+tau.^2);
end

% log base conversion
lny   = log10y * log(10);
sigma = sigma  * log(10);
phi   = phi    * log(10);
tau   = tau    * log(10);

% unit convertion
lny  = lny+log(units);

function [lny,sigma,tau,phi] = gmpe(index,Rjb,M,SOF,media,damping)

if index== 0 % PGA
    [Sa,Ss,Fn,Fr,b1,b2,b3,b4,b5,b6,b7,b8,b9,b10,c1,c2,c3,c4] = getcoeff_PGA(SOF,media);
else
    [Sa,Ss,Fn,Fr,b1,b2,b3,b4,b5,b6,b7,b8,b9,b10,c1,c2,c3,c4] = get_coeffs(index,SOF,media,damping);
end

lny    = b1 + b2*M + b3*M.^2 + (b4+b5*M).*log10(sqrt(Rjb.^2+b6^2)) + b7*Ss + b8*Sa + b9*Fn + b10*Fr;
phi    = c1 + c2*M;
tau    = c3 + c4*M;
sigma  = sqrt(phi.^2+tau.^2);

