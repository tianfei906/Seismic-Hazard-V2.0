    phi        = interp1(x,Y_sigma2,log(To))';
    tau        = interp1(x,Y_sigma1,log(To))';
    sigma      = sqrt(phi.^2+tau.^2);
end

% unit convertion
lny  = lny+log(units);

% modifier
if exist('adjfun','var')
