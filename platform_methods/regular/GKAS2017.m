T2(M>=6.5)=1+0.2*(M(M>=6.5)-6.5);
T2(M<=5.5)=0;

R1  = W*cosd(dip);
R2  = 4*R1;
Ry1 = Rx*tand(20);
h1  = 0.25;
h2  = 1.5;
h3  = -0.75;

